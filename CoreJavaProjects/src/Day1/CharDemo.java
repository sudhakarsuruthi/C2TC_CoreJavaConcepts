package Day1;

public class CharDemo {

	public static void main(String[] args) {
		char ch = 's';
		System.out.println(ch);

		
		char ch1 = 49;
		System.out.println(ch1);

		
		char var1 = '\u00A5';
		System.out.println(var1);

		
		char var2 = '\u19AC';
		System.out.println(var2);

		
		int a = 'A';
		System.out.println(a);

	}

}
